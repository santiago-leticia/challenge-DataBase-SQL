                    nm_procedure,
                    nm_usuario,
                    dt_ocorrencia,
                    codigo_erro,
                    mensagem_erro 
                )VALUES (
                    'dados_vet_clinica',
                    USER,
                    SYSDATE,
                    v_codigo_erro, 
                    v_mensagem_erro
                );
