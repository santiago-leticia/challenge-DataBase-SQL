-- Vet clinica

CREATE OR REPLACE PROCEDURE dados_vet_clinica(
    p_id_vet NUMBER,
    p_id_clinica NUMBER)
IS
    v_id_vet number :=0;
    v_id_clinica number :=0;
BEGIN
    SELECT id_vet INTO v_id_vet FROM T_CLYVO_VET
        WHERE id_vet = p_id_vet;
        
    SELECT id_clinica INTO v_id_clinica FROM T_CLYVO_CLINICA
        WHERE id_clinica = p_id_clinica;

    INSERT INTO T_CLYVO_VET_CLINICA(
        id_vet,
        id_clinica)
    VALUES (
        p_id_vet,
        p_id_clinica
    );
    EXCEPTION
        WHEN VALUE_ERROR THEN
            INSERT INTO LOGS(
                nm_procedure,
                nm_usuario,
                dt_ocorrencia,
                codigo_erro,
                mensagem_erro
            )
            values(
                'Erro na vet clinica',
                USER,
                SYSDATE,
                SQLCODE,
                'Erro em inserir vet clinica'
            );
         WHEN NO_DATA_FOUND THEN
            IF v_id_vet = 0 THEN
                INSERT INTO LOGS(
                    nm_procedure,
                    nm_usuario,
                    dt_ocorrencia,
                    codigo_erro,
                    mensagem_erro
                )
                values(
                    'dados_prescricao',
                    USER,
                    SYSDATE,
                    SQLCODE,
                    'Id de vet não encontrado na tabela de vet.'
                );
            ELSIF v_id_clinica = 0 THEN
                INSERT INTO LOGS(
                    nm_procedure,
                    nm_usuario,
                    dt_ocorrencia,
                    codigo_erro,
                    mensagem_erro
                )
                values(
                    'dados_prescricao',
                    USER,
                    SYSDATE,
                    SQLCODE,
                    'Id de clinica não encontrado na tabela de clinica.'
                );
            ELSE
                INSERT INTO LOGS(
                    nm_procedure,
                    nm_usuario,
                    dt_ocorrencia,
                    codigo_erro,
                    mensagem_erro
                )
                values(
                    'dados_prescricao',
                    USER,
                    SYSDATE,
                    SQLCODE,
                    'Id de Vet e Clinica não encontrado.'
                );
            END IF;
        WHEN OTHERS THEN
            INSERT INTO LOGS (
                nm_procedure,
                nm_usuario,
                dt_ocorrencia,
                codigo_erro,
                mensagem_erro
            )VALUES (
                'dados_vet_clinica',
                USER,
                SYSDATE,
                SQLCODE,
                SQLERRM
            );
END dados_vet_clinica;

