                    v_mensagem_erro
                );
                dbms_output.put_line('Id clinica e vet não encontrado');
            END IF;
        WHEN OTHERS THEN
            v_codigo_erro := TO_CHAR(SQLCODE);
            v_mensagem_erro := SUBSTR(SQLERRM, 1, 400);
